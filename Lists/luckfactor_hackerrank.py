

def luckBalance(k, contests):
    balance = sum([L for [L,K] in contests if K==0])
    impt_contests = sorted([[L,K] for [L,K] in contests if K==1], reverse=True)
    if len(impt_contests)>k:
        last = len(impt_contests)-1
        while last>=k:
            balance-=impt_contests[last][0]
            last-=1
    i=0
    while i<len(impt_contests) and i<k:
        balance+=impt_contests[i][0]
        i+=1
    return balance
